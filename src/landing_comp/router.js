// Simple SPA Router for Vanilla JS
class Router {
    constructor(routes) {
        this.routes = routes;
        this.currentPath = window.location.pathname;
        this.landingStylesLoaded = false;

        window.addEventListener('popstate', () => this.handleRoute());
        document.addEventListener('click', (e) => this.handleClick(e));
    }

    async ensureLandingStyles() {
        if (this.landingStylesLoaded) return;

        // styles/style.css is the official stylesheet for the landing.
        // styles/globlas.css belongs to a different Tailwind variant and
        // should not be loaded in parallel with this one.
        await import('../styles/style.css');
        this.landingStylesLoaded = true;
    }

    handleClick(e) {
        const link = e.target.closest('a[href^="/"]');
        if (link && !link.hasAttribute('data-external')) {
            e.preventDefault();
            const path = link.getAttribute('href');
            this.navigate(path);
        }
    }

    navigate(path) {
        if (path !== this.currentPath) {
            window.history.pushState({}, '', path);
            this.currentPath = path;
            this.handleRoute();
        }
    }

    async handleRoute() {
        const path = window.location.pathname;
        this.currentPath = path;

        const route = this.routes[path] || this.routes['/'];
        const app = document.getElementById('app');

        if (!app) return;

        await this.ensureLandingStyles();

        app.classList.remove('hidden');
        app.style.opacity = '0';
        app.style.transition = 'opacity 0.2s ease';

        setTimeout(() => {
            app.innerHTML = route();
            app.style.opacity = '1';
            this.initializeInteractivity();

            if (window.location.hash) {
                const target = document.querySelector(window.location.hash);
                if (target) {
                    target.scrollIntoView({ behavior: 'smooth', block: 'start' });
                    return;
                }
            }

            window.scrollTo({ top: 0, behavior: 'smooth' });
        }, 200);
    }

    initializeInteractivity() {
        const contactForm = document.getElementById('contact-form');
        if (contactForm) {
            const newForm = contactForm.cloneNode(true);
            contactForm.parentNode.replaceChild(newForm, contactForm);

            newForm.addEventListener('submit', (e) => {
                e.preventDefault();

                const formContainer = document.getElementById('form-container');
                if (!formContainer) return;

                formContainer.innerHTML = `
                    <div class="flex flex-col items-center justify-center py-12 text-center animate-fade-in-up">
                        <div class="mb-4 flex h-16 w-16 items-center justify-center rounded-full bg-gradient-to-br from-green-500 to-emerald-600 shadow-lg shadow-green-500/30">
                            ${icons.checkCircle}
                        </div>
                        <h3 class="mb-2 text-xl font-semibold text-foreground">Message Sent!</h3>
                        <p class="text-muted-foreground">Thank you for reaching out. We'll get back to you soon.</p>
                    </div>
                `;
            });
        }

        const googleSignInBtn = document.getElementById('google-signin-btn');
        if (googleSignInBtn && this.onLogin) {
            const newBtn = googleSignInBtn.cloneNode(true);
            googleSignInBtn.parentNode.replaceChild(newBtn, googleSignInBtn);

            newBtn.addEventListener('click', async () => {
                newBtn.innerHTML = `<span>Cargando Elai...</span>`;
                newBtn.disabled = true;

                try {
                    await this.onLogin();
                } catch (err) {
                    console.error(err);
                    newBtn.innerHTML = `<span>Continue with Google</span>`;
                    newBtn.disabled = false;
                }
            });
        }
    }

    setLoginHandler(handler) {
        this.onLogin = handler;
    }

    init() {
        this.handleRoute();
    }
}

